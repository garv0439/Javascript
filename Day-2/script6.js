const week = "tues";
switch (week) {
    case "monday":
        console.log("monday is week day");
        break;
    case "tuesday":
        console.log("tuesday is week day");
        break;
    case "wednesday":
        console.log("wednesday is week day");
        break;
    case "thursday":
        console.log("thursday is week day");
        break;
    case "friday":
        console.log("friday is week day");
        break;
    case "saturday":
        console.log("saturday is week day");
        break;
    case "sunday":
        console.log("sunday is week day");
        break;
    default:
        console.log("enter correct week days")
        break;
}